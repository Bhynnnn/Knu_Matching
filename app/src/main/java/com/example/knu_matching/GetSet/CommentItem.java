package com.example.knu_matching.GetSet;

public class CommentItem {
    String str_NickName;
    String str_Content;
    String str_Date;
    String str_Email;

    public String getStr_Email() {
        return str_Email;
    }

    public void setStr_Email(String str_Email) {
        this.str_Email = str_Email;
    }

    public String getStr_NickName() {
        return str_NickName;
    }

    public void setStr_NickName(String str_NickName) {
        this.str_NickName = str_NickName;
    }

    public String getStr_Content() {
        return str_Content;
    }

    public void setStr_Content(String str_Content) {
        this.str_Content = str_Content;
    }

    public String getStr_Date() {
        return str_Date;
    }

    public void setStr_Date(String str_Date) {
        this.str_Date = str_Date;
    }
}
